public class DateBaseModle {
    //表名
    private String url;
    //表描述
    private String scamer;
//    列名
    private String user;
//    数据类型
    private String password;
//    数据长度

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getScamer() {
        return scamer;
    }

    public void setScamer(String scamer) {
        this.scamer = scamer;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
